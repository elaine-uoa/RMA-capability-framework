export function Navbar() {
  const pathname = usePathname();

  // Check if we're on a page that matches (handle query params)
  const isActive = (href: string) => {
    if (href === "/") {
      return pathname === "/";
    }
    return pathname.startsWith(href);
  };

  return (
    <nav className="w-full bg-white border-b border-slate-200 sticky top-0 z-50 no-print flex justify-center">
      <div className="w-full max-w-4xl px-8 sm:px-12 lg:px-16">
        <div className="flex items-center justify-center h-14">
          {/* Navigation Links - Centered */}
          <div className="flex items-center gap-1">
            {navItems.map((item) => {
              const active = isActive(item.href);
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  className={`
                    inline-flex items-center gap-1.5 px-3 py-2 rounded-lg text-sm font-medium transition-colors
                    ${active
                      ? "bg-indigo-50 text-indigo-700"
                      : "text-slate-600 hover:text-slate-900 hover:bg-slate-100"
                    }
                  `}
                >
                  {icons[item.icon]}
                  <span className="hidden md:inline">{item.label}</span>
                </Link>
              );
            })}
          </div>
        </div>
      </div>
    </nav>
  );
}
